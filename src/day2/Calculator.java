package day2;

public class Calculator {

    public int plus(int num1 , int num2){
        return num1 + num2;
    }
    public int minus(int num1 , int num2){
        return num1 - num2;
    }
    public int multiple(int num1 , int num2){
        return num1 * num2;
    }
    public float divide(int num1 , float num2){
        return num1 / num2;
    }
}
